package br.com.caelum.delfos.graphs.mappers;

public class Pair {

    private Integer from;
    private Integer to;

    public Pair(Integer from, Integer to) {
        this.from = from;
        this.to = to;
    }

    public Integer getFrom() {
        return from;
    }

    public Integer getTo() {
        return to;
    }
}
